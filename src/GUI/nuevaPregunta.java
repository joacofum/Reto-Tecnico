    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String pregunta = txtPregunta.getText();
        String respuestaCorrecta = txtRespuestaCorrecta.getText();
        String respuestaIncorrecta1 = txtRespuestaIncorrecta1.getText();
        String respuestaIncorrecta2 = txtRespuestaIncorrecta2.getText();
        String respuestaIncorrecta3 = txtRespuestaIncorrecta3.getText();
            
        
        
        //Chequeo valores vac√≠os.
        if(pregunta.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar la pregunta", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }else if(respuestaCorrecta.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar la respuesta correcta", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }else if(respuestaIncorrecta1.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar la respuesta incorrecta 1", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }else if(respuestaIncorrecta2.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar la respuesta incorrecta 2", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }else if(respuestaIncorrecta3.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar la respuesta incorrecta 3", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }else if(this.txtPuntosCategoria.getText().isEmpty()){
            
        }else{
            int puntosCategoria = Integer.valueOf(this.txtPuntosCategoria.getText());
            int nivelCategoria = Integer.valueOf(this.cbxNivelCategoria.getSelectedItem().toString());
            
            //Creo la pregunta
            Pregunta p = new Pregunta();
            p.setDescripcion(pregunta);
            p.setNivel(nivelCategoria);
            p.setPuntos(puntosCategoria);
            p.setCategoria((Categoria) cboCategoria.getSelectedItem());
            
            //Creo las respuestas
            Respuesta r1 = new Respuesta();
            Respuesta r2 = new Respuesta();
            Respuesta r3 = new Respuesta();
            Respuesta r4 = new Respuesta();

            r1.setDescripcion(respuestaCorrecta);
            r1.setIsCorrecta(true);

            r2.setDescripcion(respuestaIncorrecta1);
            r2.setIsCorrecta(false);

            r3.setDescripcion(respuestaIncorrecta2);
            r3.setIsCorrecta(false);

            r4.setDescripcion(respuestaIncorrecta3);
            r4.setIsCorrecta(false);

            List<Respuesta> respuestas = new ArrayList<>(); 
            respuestas.add(r1);
            respuestas.add(r2);
            respuestas.add(r3);
            respuestas.add(r4);                 
            p.setRespuestas(respuestas);
            
            boolean exito = Conexion.getInstance().persist(p);
            if(exito){           
                //Muestro alerta y limpio los campos...
                javax.swing.JOptionPane.showMessageDialog(this, "Se ha creado la pregunta correctamente.");
                this.txtPregunta.setText("");
                this.txtRespuestaCorrecta.setText("");
                this.txtRespuestaIncorrecta1.setText("");
                this.txtRespuestaIncorrecta2.setText("");
                this.txtRespuestaIncorrecta3.setText("");
                this.txtPuntosCategoria.setText("");    
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

